package org.example.class1;

public class VariableValues {
    public static void main(String[] args) {
        byte age=20;
        System.out.println(age);
        age=21;
        System.out.println(age);
        short number=400;
        System.out.println(number);
        number=200;
        System.out.println(number);
        int population=900000000;
        System.out.println(population);
        population=800000000;
        System.out.println(population);
        long distanceFromSun=12555555589077L;
        System.out.println(distanceFromSun);
        distanceFromSun=10555555589077L;
        System.out.println(distanceFromSun);
        float price=12.3f;
        System.out.println(price);
        price=14.9f;
        System.out.println(price);
        double stars=12.66666666456;
        System.out.println(stars);
        stars=13.66666666456;
        System.out.println(stars);
        char gender='W';
        System.out.println(gender);
        gender='M';
        System.out.println(gender);
        boolean isSnow=true;
        System.out.println(isSnow);
        isSnow=true;
        System.out.println(isSnow);


    }
}
