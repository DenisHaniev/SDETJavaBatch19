package org.example.class2;

public class E6ArithmeticOperator {
    public static void main(String[] args) {
        int num1=100;
        int num2=20;
        int results=num1-num2;
        System.out.println(results);

        double num3=100.5;
        double num4=20.8;
        double results1 =num3-num4;
        System.out.println(results1);
    }
}
