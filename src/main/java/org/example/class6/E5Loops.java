public class E5Loops {
    public static void main(String[] args) {
        // нам надо 5 раз прогнать код что он будеть считать за праду  от 10.11.12.13.14
        // если прогнать без оператора "+" тогда бесконечное кол-во
        int num = 10;
        while (num>15) {

            System.out.println(num);
            num++;

        }
    }
}









