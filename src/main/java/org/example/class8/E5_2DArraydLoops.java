package org.example.class8;

public class E5_2DArraydLoops {

    public static void main(String[] args) {


              /*
                   0   1  2  3 -Colum
             0 ROW 10 20 40 50
             1 ROW 22 40 90 65
             2 ROW 15 50 33 30


                        */
        for (int j = 0; j <7; j++) {   // j<7 ROWS
            for (int i = 0; i <4; i++) {  // i<4 Coloms
                System.out.print("*");
            }
            System.out.println();
        }
    }
}










